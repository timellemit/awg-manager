#!/usr/bin/env node
//
// Generates src/lib/generated/qureIcons.ts — a frozen array of Koolson Qure
// icon names from the upstream GitHub repo (IconSet/Color directory).
//
// WHEN TO RUN:
//   1. To pick up newly added icons in Qure upstream
//   2. To remove icons that were deleted upstream
//
// HOW TO RUN:
//   cd frontend
//   node scripts/generate-qure-icons.mjs
//   git add src/lib/generated/qureIcons.ts
//
// NOTE: GitHub API rate-limits unauthenticated requests to 60/hour per IP.
//   If you hit the limit, set GITHUB_TOKEN env var (read-only is enough).

import { writeFile, mkdir } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const OUT = resolve(ROOT, 'src/lib/generated/qureIcons.ts');

const API = 'https://api.github.com/repos/Koolson/Qure/contents/IconSet/Color?ref=master';
const CDN_BASE = 'https://cdn.jsdelivr.net/gh/Koolson/Qure@master/IconSet/Color';

async function fetchIconList() {
	const headers = { 'User-Agent': 'awg-manager-icon-generator' };
	if (process.env.GITHUB_TOKEN) {
		headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
	}
	const res = await fetch(API, { headers });
	if (!res.ok) {
		throw new Error(`GitHub API ${res.status}: ${await res.text()}`);
	}
	const items = await res.json();
	return items
		.filter((it) => it.type === 'file' && it.name.endsWith('.png'))
		.map((it) => it.name.replace(/\.png$/, ''))
		.sort((a, b) => a.localeCompare(b));
}

const HEADER = `// AUTO-GENERATED by scripts/generate-qure-icons.mjs — do not edit by hand.
// Run \`node scripts/generate-qure-icons.mjs\` to refresh.
`;

const TEMPLATE = (names) => `${HEADER}
export const QURE_CDN_BASE = '${CDN_BASE}';

export const QURE_ICONS: readonly string[] = ${JSON.stringify(names, null, '\t')} as const;

/** Build the CDN URL for a given Qure icon name (without .png extension). */
export function qureIconUrl(name: string): string {
\treturn \`\${QURE_CDN_BASE}/\${encodeURIComponent(name)}.png\`;
}

/**
 * Find an exact case-insensitive match of \`name\` in the Qure catalog.
 * Spaces are normalized to underscores before matching.
 * Returns the canonical icon name (preserving case) or null.
 */
export function qureMatchByName(name: string): string | null {
\tconst normalized = name.trim().replace(/\\s+/g, '_').toLowerCase();
\tif (!normalized) return null;
\tfor (const icon of QURE_ICONS) {
\t\tif (icon.toLowerCase() === normalized) return icon;
\t}
\treturn null;
}
`;

async function main() {
	console.log('Fetching Qure icon list from GitHub API...');
	const names = await fetchIconList();
	console.log(`Found ${names.length} icons`);
	await mkdir(dirname(OUT), { recursive: true });
	await writeFile(OUT, TEMPLATE(names));
	console.log(`Wrote ${OUT}`);
}

main().catch((err) => {
	console.error(err);
	process.exit(1);
});
